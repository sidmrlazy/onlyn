    <p>
        Dr. Neeraj Bora, a prominent politician and social worker, serves as an MLA in the seventeenth assembly of Uttar
        Pradesh. Besides his role as an MLA, he holds the position of a Member of the Committee on Examination of Audit
        Reports of Local Bodies in the Uttar Pradesh Legislative Assembly. In the 2017 Vidhansaba elections, he achieved
        a significant victory by defeating the strongman minister of the Samajwadi Party in the crucial Lucknow Uttar
        constituency, which had been dominated by other parties for the past decade.</p>

    <p>The tradition of public service runs deep in Dr. Bora's family, as his father, Smriti Shesh Dr. D. P. Bora Ji,
        also
        served as an MLA and esteemed politician in the Uttar Pradesh Legislative Assembly in 1969 and 1977. Born on
        March 31, 1967, Dr. Neeraj Bora not only carried forward his father's legacy but also went beyond it, utilizing
        every opportunity to serve the people.</p>
