    <h2><span>INNOVATIVE</span> CURRICULUM</h2>
    <p>At Jaipuria, we always have the child at the center of all our processes. We also believe in the power of
        self:
        Self-knowledge, Self-control, Self-assessment and Self-learning. These are the mainstays of the
        Curriculum @
        Jaipuria.</p>

    <p>We have also developed a unique pedagogy which encourages learning more than mere completion of tasks,
        innovatively named ‘NO SCHOOL’. The idea is that our students should look at teaching-learning as a way
        of
        life,
        not as a compulsion. NO SCHOOL infuses the spirit of experiential learning by fun, thereby creating
        lifelong
        learners. We have aimed to nurture the upcoming generation with all-round development of mind, body and
        soul.
        The curriculum aspires to create self-thinking, reflective individuals who have the courage to stop,
        think
        and
        reflect at every step of their educational journey.</p>

    <p>The NO SCHOOL pedagogy has been intentionally divided into four phases:</p>

