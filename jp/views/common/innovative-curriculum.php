    <div class="mt-5">
        <p>The benchmark for the curriculum development process was National Curriculum Framework (NCF 2005), which has
            a detailed structured approach for each subject. To enhance the content and make it more holistic, we also
            took important lessons from international curricular standards (mainly the Common Core standards from USA,
            the National Curriculum from UK, Finland and Sweden etc.).</p>

        <p>In today’s context wherein the world is getting increasingly competitive and demands innovation from every
            student, the curriculum entails the learning level for each student. It defines the cognitive level at which
            students will be able to operate with the content. It is influenced by many leading taxonomies, including
            educational philosophers such as Bloom, Anderson, Robert Marzano and John Kendall. We are living in an era
            where we are preparing students for a society that we have not yet seen, and can only imagine.</p>

        <p><strong>Therefore, 21st Century Skills become important.</strong></p>

        <p>Today, we need a curriculum, which should not only be student-centric, however, unprecedented amongst the
            existing ones. The teacher is required to achieve the aims of education. For that purpose, we must enable
            them to employ suitable instructional methods and procedures, and that is the purpose of the Jaipuria
            curriculum!</p>
